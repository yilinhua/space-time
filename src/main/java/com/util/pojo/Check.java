package com.util.pojo;

public class Check {
    private int id; 
	private String check_time;
    private String check_type;
    private String check_name;
    private int practical_repertory;
    private int system_repertory;
    private double price;
    private int qty;
    private String specification;
    private int drug_type;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCheck_time() {
		return check_time;
	}
	public void setCheck_time(String check_time) {
		this.check_time = check_time;
	}
	
	public String getCheck_type() {
		return check_type;
	}
	public void setCheck_type(String check_type) {
		this.check_type = check_type;
	}
	public String getCheck_name() {
		return check_name;
	}
	public void setCheck_name(String check_name) {
		this.check_name = check_name;
	}
	public int getPractical_repertory() {
		return practical_repertory;
	}
	public void setPractical_repertory(int practical_repertory) {
		this.practical_repertory = practical_repertory;
	}
	public int getSystem_repertory() {
		return system_repertory;
	}
	public void setSystem_repertory(int system_repertory) {
		this.system_repertory = system_repertory;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	public int getQty() {
		return qty;
	}
	public void setQty(int qty) {
		this.qty = qty;
	}
	public String getSpecification() {
		return specification;
	}
	public void setSpecification(String specification) {
		this.specification = specification;
	}
	public int getDrug_type() {
		return drug_type;
	}
	public void setDrug_type(int drug_type) {
		this.drug_type = drug_type;
	}
	
    
	
}
