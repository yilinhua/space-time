package com.util.pojo;

public class renyuan {

	private int id;
	private String time;
	private int xin;
	private int lao;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public int getXin() {
		return xin;
	}

	public void setXin(int xin) {
		this.xin = xin;
	}

	public int getLao() {
		return lao;
	}

	public void setLao(int lao) {
		this.lao = lao;
	}

}
