package com.util.pojo;

public class Merchants {
   private int id;
   private String affiliated_Company;
   private int merchants_Type;
   private String contact;
   private String mailbox;
   private String telephone;
   private String mobile_phone;
   private String address;
   private String postal_code;
   private String alipay;
   private String fax;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getAffiliated_Company() {
	return affiliated_Company;
}
public void setAffiliated_Company(String affiliated_Company) {
	this.affiliated_Company = affiliated_Company;
}
public int getMerchants_Type() {
	return merchants_Type;
}
public void setMerchants_Type(int merchants_Type) {
	this.merchants_Type = merchants_Type;
}
public String getContact() {
	return contact;
}
public void setContact(String contact) {
	this.contact = contact;
}
public String getMailbox() {
	return mailbox;
}
public void setMailbox(String mailbox) {
	this.mailbox = mailbox;
}
public String getTelephone() {
	return telephone;
}
public void setTelephone(String telephone) {
	this.telephone = telephone;
}
public String getMobile_phone() {
	return mobile_phone;
}
public void setMobile_phone(String mobile_phone) {
	this.mobile_phone = mobile_phone;
}
public String getAddress() {
	return address;
}
public void setAddress(String address) {
	this.address = address;
}
public String getPostal_code() {
	return postal_code;
}
public void setPostal_code(String postal_code) {
	this.postal_code = postal_code;
}
public String getAlipay() {
	return alipay;
}
public void setAlipay(String alipay) {
	this.alipay = alipay;
}
public String getFax() {
	return fax;
}
public void setFax(String fax) {
	this.fax = fax;
}
   
   
}
